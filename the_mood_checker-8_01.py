mood = input("What is your mood today? ")

if mood == "happy":
    print("Great, good to hear you are well :) ")
elif mood == "sad":
    print("Dont't worry, the sun will shine again for you soon. ")
elif mood == "nervous":
    print("Just take a deep breath and relax..")
elif mood == "excited":
    print("Yeah! :) ")
else:
    print("Sorry, I don't recognize this mood.")